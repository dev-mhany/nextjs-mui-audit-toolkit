function getProjectStatus(score) {
  if (score >= 90) return 'ðŸŸ¢ Excellent - Ready for production'
  if (score >= 80) return 'ðŸŸ¡ Good - Minor improvements needed'
  if (score >= 70) return 'ðŸŸ  Fair - Several issues to address'
  if (score >= 60) return 'ðŸ”´ Poor - Significant work required'
  return 'âš« Critical - Must fix before deployment'
}

